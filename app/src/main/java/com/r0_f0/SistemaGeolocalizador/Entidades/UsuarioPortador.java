package com.r0_f0.SistemaGeolocalizador.Entidades;

public class UsuarioPortador {


    private String idPortador;
    private String nombre;
    private String appPat;

    public String getIdPortador() {
        return idPortador;
    }

    public void setIdPortador(String idPortador) {
        this.idPortador = idPortador;
    }


    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getAppPat() {
        return appPat;
    }

    public void setAppPat(String appPat) {
        this.appPat = appPat;
    }
}
